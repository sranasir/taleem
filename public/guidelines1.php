	<u><h3>Educational Loan:</h3></u>
	<div id="details" class="guidelines-section">
		<b>Overview:</b>
		<br>
		This program offers a limited number of loans to support college education
		for Ahmadi Students of USA Jamat. Kindly note that only need-based loans are awarded.
		You will need to provide, to the best of your knowledge, an estimate of the finances required
		as well as your current financial standing.
		<br>
		The amount of loan varies from $500-$5000 according to availability of funds and the number of
		deserving applicants. It is paid in one installment and the loan check will be paid in the applicant's
		name. Please note that due to large number of applicants, it is not possible to support 100% of the 
		educational need in a specific case. In most of the cases, the goal of Jamat is to supplement
		the studentâ€™s resources and provide some help. The loan is returnable after the applicant has 
		finished the education on a mutually agreeable time scale. Repayment should be made to the 
		local finance secretary, who should record the amount in the shaded area as <i>loan repayment</i>.		 
		<br>
		<br>
		**If you are an African American student, there is a special fund for this purpose and you should apply
