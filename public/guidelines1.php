<div id="TalentAward Application">
	<u><h3>Application for Ahmadiyya Talent Award</h3></u>
	<div id="details" class="guidelines-section">
		<b>Overview:</b>
		<br>
		There are 3 categories of the award without an associated monetary award:
		<ul>
			<li>
				<u>Talent Award Gold Medal:</u> This is given to the best candidates.
				They are typically in the top 3 percent of their group e.g. SAT >97%, first in school,
				cumulative GPA of 4 from a good university, top of class in graduate studies, professionals 
				with national recognitions, etc. They would receive a gold medal and certificate on stage at 
				the time of Jalsa.
			</li>
			<li>
				<u>Talent Recognition Award:</u> This is given to the 2nd best candidates. They are typically 
				in the top 5% of their group e.g. SAT 95%, GPA 3.8, professionals with some national and local
				recognition, etc. They would receive a certificate on stage at the time of Jalsa.
			</li>
			<li>
				<u>Talent Award Finalist:</u> This is given to candidates who were strongly considered for the 
				award but were not given as they were felt to be below the top 5% of their group. They would 
				have their certificates mailed to them and their names would be displayed during Jalsa.
			</li>						
		</ul>
		<br>
		Applicants can apply in one of 4 categories: highschool, undergraduate, graduate and working
		professional. For educational categories, the applicant must have completed their course of study 
		within the last 3 years in the US and cannot apply more than once for the same educational category.
		For the professional category, there is no time limit for including previous accomplishments to highlight
		the progress and achievements made in the respective field. Professional applications may be resubmitted
		in subsequent years if additional progress/achievements merit another review. Applicants who have not completed 
		a full degree (e.g. middle school, 10th grade, associate degree, vocational courses) are not eligible. The selection committee will 
		assess multiple criteria, including but not limited to the ones mentioned in the following lines for 
		individual categories:
		<br>
		<ul>
			<li>
				<u>High school completion:</u> SAT/ACT scores, other standardized national exams, high school GPA, class 
				rank, scholarships received, leadership and extracurricular activities.
			</li>
			<li>
				<u>Undergraduate completion:</u> GPA, class rank, post graduate entrance exams, other standardized 
				national exams, scholarships, honors and awards achieved, and publications printed.
			</li>
			<li>
				<u>Postgraduate completion:</u> GPA, class rank, post graduate entrance exams, scholarships, honors 
				and awards achieved, publications printed, thesis and other research publications if applicable.
			</li>
			<li>
				<u>Working Professionals:</u> Education Background, recognition by industry peers and/or industry associations; contributions to 
				the field; promotions and advancements in the professional field; publications, presentations, awards,
				and patents received where applicable.
			</li>
		</ul>

